// Standard Africa bbox: lng[-18, 52], lat[38, -35].
// Project to viewBox (0..1000 x 0..1100) — equirectangular.
export function project(lng: number, lat: number, width = 1000, height = 1100) {
  const x = ((lng + 18) / 70) * width;
  const y = ((38 - lat) / 73) * height;
  return { x, y };
}

export function pathFromPolygon(polygon: [number, number][], width: number, height: number) {
  return polygon.map((p, i) => {
    const { x, y } = project(p[0], p[1], width, height);
    return `${i === 0 ? "M" : "L"}${x.toFixed(1)},${y.toFixed(1)}`;
  }).join(" ") + " Z";
}
